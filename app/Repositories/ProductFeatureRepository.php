    public function store(Array $array)
    {
        // dd($array);
        DB::beginTransaction();

        try {
            $data = new ProductFeature();
            $data->product_id = $array['product_id'];
            $data->position = $array['position'];
            $data->status = 1;
            $data->save();

            DB::commit();

            return [
                'code' => 200,
                'status' => 'success',
                'message' => 'Changes have been saved',
                'data' => $data,
            ];
        } catch (\Exception $e) {
            DB::rollback();

            return [
                'code' => 500,
                'status' => 'error',
                'message' => 'An error occurred while storing data.',
                // 'message' => $e->getMessage(),
                'error' => $e->getMessage(),
            ];
        }
    }

    public function delete(Int $id)
    {
        try {
            $data = $this->getById($id);

            if ($data['code'] == 200) {
                // Handling trash
                $this->trashRepository->store([
                    'model' => 'ProductFeature',
                    'table_name' => 'product_features',
                    'deleted_row_id' => $data['data']->id,
                    'thumbnail' => $data['data']->image_s ?? null,
                    'title' => $data['data']->product->title,
                    'description' => $data['data']->product->title.' FEATURE data deleted from product_features table',
                    'status' => 'deleted',
                ]);

                $data['data']->delete();

                return [
                    'code' => 200,
                    'status' => 'success',
                    'message' => 'Data deleted',
                    'data' => $data,
                ];
            } else {
                return $data;
            }
        } catch (\Exception $e) {
            return [
                'code' => 500,
                'status' => 'error',
                'message' => 'An error occurred while deleting data.',
                'error' => $e->getMessage(),
            ];
        }
    }

